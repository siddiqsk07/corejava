package net.konic.corejava.arrays;

public class ArrayExamp3 {
	public static void main(String[] args) {
		int[] s = { 2, 4, 6, 8 };

		for (int num : s) {
			System.out.println(num);
		}

		String[] name = { "Siddiq", "Shaik" };
		for (String Full_Name : name) {
			System.out.println(Full_Name);
		}

	}

}
